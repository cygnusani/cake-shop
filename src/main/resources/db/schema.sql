CREATE TABLE CAKE (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 10, INCREMENT BY 1) NOT NULL PRIMARY KEY,
  NAME VARCHAR(50) NOT NULL,
  PRICE DECIMAL(10,2) NOT NULL,
  AVAILABLE BOOLEAN NOT NULL
);

CREATE TABLE "ORDER" (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 10, INCREMENT BY 1) NOT NULL PRIMARY KEY,
  CUSTOMER_NAME VARCHAR(50) NOT NULL,
  PRICE DECIMAL(10,2) NOT NULL,
  STATUS_CODE VARCHAR(25) NOT NULL
);

CREATE TABLE ORDER_CAKE (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 10, INCREMENT BY 1) NOT NULL PRIMARY KEY,
  ORDER_ID INTEGER NOT NULL,
  CAKE_ID INTEGER NOT NULL,
  AMOUNT INTEGER NOT NULL
);

ALTER TABLE ORDER_CAKE ADD FOREIGN KEY (ORDER_ID) REFERENCES "ORDER"(ID);
ALTER TABLE ORDER_CAKE ADD FOREIGN KEY (CAKE_ID) REFERENCES CAKE(ID);